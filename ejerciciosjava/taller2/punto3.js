let numero = Number(prompt("Ingrese un numero"));
switch (numero) {
    case 1:
        console.log("1 = uno");
        break;
    case 2:
        console.log("2 = dos");
        break;
    case 3:
        console.log("3 = tres");
        break;
    case 4:
        console.log("4 = cuatro");
        break;
    case 5:
        console.log("5 = cinco");
        break;
    case 6:
        console.log("6 = seis");
        break;
    case 7:
        console.log("7 = siete");
    case 8:
        console.log("8 = ocho");
        break;
    case 9:
        console.log("9 = nueve");
        break;
    case 10:
        console.log("10 = diez");
        break;
    default:
        console.log("Digite un numero valido");
        break;
}