let numerosletras = parseInt(prompt("Ingrese un numero del 1-10"));
switch (numerosletras) {
    case 1:
        alert("1 Uno");
        break;
    case 2:
        alert("2 Dos");
        break;
    case 3:
        alert("3 Tres");
        break;
    case 4:
        alert("4 Cuatro");
        break;
    case 5:
        alert("5 Cinco");
        break;
    case 6:
        alert("6 Seis");
        break;
    case 7:
        alert("6 Siete");
        break;
    case 8:
        alert("8 Ocho");
        break;
    case 9:
        alert("9 Nueve");
        break;
    case 10:
        alert("10 Diez");
        break;
    default:
        alert("el numero ingresado no esta dentro del rango indicado, recuerde que es del 1-10")
}   