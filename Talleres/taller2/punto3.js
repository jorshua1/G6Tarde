let numero = Number(prompt("Escriba un número del 1 al 10."))

switch(numero){
    case 1:
        alert("Uno");
        break;
    case 2:
        alert("Dos");
        break;
    case 3:
        alert("Tres");
        break;
    case 4:
        alert("Cuatro");
        break;
    case 5:
        alert("Cinco");
        break;
    case 6:
        alert("Seis");
        break;
    case 7:
        alert("Siete");
        break;
    case 8:
        alert("Ocho");
        break;
    case 9:
        alert("Nueve");
        break;
    case 10:
        alert("Diez");
        break;
    default:
        alert("Error, el número indicado no está permitido");
        break;
}
