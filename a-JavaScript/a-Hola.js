/*                                                                 Variables
Utiliza variables como nombres simbólicos para valores en tu aplicación. 
Los nombres de las variables, llamados identificadores, se ajustan a ciertas reglas.
Un identificador de JavaScript debe comenzar con una letra, un guión bajo (_) o un signo de dólar ($).
Los siguientes caracteres también pueden ser dígitos (0-9).
Dado que JavaScript distingue entre mayúsculas y minúsculas, las letras incluyen los caracteres "A" a "Z" (mayúsculas),
así como "a" a "z" (minúsculas).
Puedes utilizar la mayoría de las letras ISO 8859-1 o Unicode como å y ü en los identificadores. 
También puedes usar Secuencias de escape Unicode como caracteres en identificadores.
Algunos ejemplos de nombres legales son:
Number_hits.
temp99
$credit
_name.
*/

