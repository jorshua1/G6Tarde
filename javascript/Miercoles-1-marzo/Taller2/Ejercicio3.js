let x = Number(prompt("ingrese un numero"));
switch (x) {
    case 1:
        alert("UNO");
        break;
    case 2:
        alert("DOS");
        break;
    case 3:
        alert("TRES");
        break;
    case 4:
        alert("CUATRO");
        break;
    case 5:
        alert("CINCO");
        break;
    case 6:
        alert("SEIS");
        break;
    case 7:
        alert("SIETE");
        break;
    case 8:
        alert("OCHO");
        break;
    case 9:
        alert("NUEVE");
        break;
    case 10:
        alert("DIEZ");
        break;
    default:
        alert("INGRESE UN NUMERO VALIDO");                                
}