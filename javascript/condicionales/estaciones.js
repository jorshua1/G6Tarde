let mes = Number(prompt("Seleccione un número de 1-12 de acuerdo a la posición del mes en el calendario "))

if(mes == 1){
    alert("Enero está en Invierno")
}else if(mes == 2){
    alert("Febrero está en Invierno")
}else if(mes == 3){
    alert("Marzo está en Invierno")
}else if(mes == 4){
    alert("Abril está en Primavera")
}else if(mes == 5){
    alert("Mayo está en Primavera")
}else if(mes == 6){
    alert("Junio está en Primavera")
}else if(mes == 7){
    alert("Julio está en verano")
}else if(mes == 8){
    alert("Agosto está en verano")
}else if(mes == 9){
    alert("Septiembre está en verano")
}else if(mes == 10){
    alert("Octubre está en Otoño")
}else if(mes == 11){
    alert("Noviembre está en Otoño")
}else if(mes == 12){
    alert("Diciembre está en Otoño")
}else{
    alert("Introduzca un valor de mes válido")
}