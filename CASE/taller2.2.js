alert('ingrese un numero comprendido entre el 1 y el 10')
let numero = parseInt(prompt('ingrese el numero'))
switch (numero) {
  case 1:
    alert('numero uno')
    break
  case 2:
    alert('numero dos')
    break
  case 3:
    alert('numero tres')
    break
  case 4:
    alert('numero cuatro')
    break
  case 5:
    alert('numero cinco')
    break
  case 6:
    alert('numero seis')
    break
  case 7:
    alert('numero siete')
    break
  case 8:
    alert('numero ocho')
    break
  case 9:
    alert('numero nueve')
    break
  case 10:
    alert('numero diez')
  default:
    alert('error')
}
