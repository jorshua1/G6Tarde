let numero=Number(prompt("Ingrese un numero comprendido entre 1-10"));

switch(numero){
    case 1:
        alert(numero+" = UNO")
        break;
    case 2:
        alert(numero+" = DOS")
        break;
    case 3:
        alert(numero+" = TRES")
        break;
    case 4:
        alert(numero+" = CUATRO")
        break;
    case 5:
        alert(numero+" = CINCO")
        break;
    case 6:
        alert(numero+" = SEIS")
        break;
    case 7:
        alert(numero+" = SIETE")
        break;
    case 8:
        alert(numero+" = OCHO")
        break;
    case 9:
        alert(numero+" = NUEVE")
        break;
    case 10:
        alert(numero+" = DIEZ")
        break;
    default:
        alert("ERROR");
}