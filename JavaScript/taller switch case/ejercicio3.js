let numero= Number(prompt("Ingrese un numero (1 a 10)"));
switch(numero) {
    case 1:
        alert("uno")
    break;
    case 2:
        alert("dos")
    break;
    case 3:
        alert("tres")
    break;
    case 4:
        alert("cuatro")
    break;
    case 5:
        alert("cinco")
    break;
    case 6:
        alert("seis")
    break;
    case 7:
        alert("siete")
    break;
    case 8:
        alert("ocho")
    break;
    case 9:
        alert("nueve")
    break;
    case 10:
        alert("diez")
    break;
    default:
        alert("No es un numero valido")
}