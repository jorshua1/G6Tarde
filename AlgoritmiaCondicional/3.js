let numero = Number(prompt("ingrese un numero del 1 al 10"))

switch (numero){
case 1: alert ("el numero es uno")
break;
case 2: alert ("el numero es dos")
break;
case 3: alert ("el numero es tres")
break;
case 4: alert ("el numero es cuatro")
break;
case 5: alert ("el numero es cinco")
break;
case 6: alert ("el numero es seis")
break;      
case 7: alert ("el numero es siete")
break;
case 8: alert ("el numero es ocho")
break;
case 9: alert ("el numero es nueve")
break;
case 10: alert ("el numero es diez")
default: alert ("error")
}  
