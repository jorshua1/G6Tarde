let num = parseInt(prompt("por favor ingresar un numero entre 1 y 10"));

switch (num){
    case 1:
        alert("1 = uno");
        break;
    case 2:
        alert("2 = doa");
        break;
    case 3:
        alert("3 = tres");
        break;
    case 4:
        alert("4 = cuatro");
        break;
    case 5:
        alert("5 = cinco");
        break;
    case 6:
        alert("6 = seis");
        break;
    case 7:
        alert("7 = siete");
        break;
    case 8:
        alert("8 = ocho");
        break;
    case 9:
        alert("9 = nueve");
        break;
    case 10: 
        alert("10 = dies");
        break;
    default:
        alert("El numero no esta entre 1 y 10 o no se esta escribiendo un numero");
}