let num=Number(prompt("ingrese el numero"))

switch (num){
    case 1:
        console.log("uno");
        break
    case 2:
        console.log("dos");
        break
    case 3:
        console.log("tres");
        break
    case 4:
        console.log("cuatro");
    case 5:
        console.log("cinco");
        break
    case 6:
        console.log("seis");
        break
    case 7:
        console.log("siete");
        break
    case 8:
        console.log("ocho");
        break
    case 9:
        console.log("nueve");
        break
    case 10:
        console.log("dies");
        break
    default:
        console.log("no es un numero del 1 al 10");
}